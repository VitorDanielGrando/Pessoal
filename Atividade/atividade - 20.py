# Faça um algoritmo que calcule e mostre a área de um losango. Sabe-se que: A = (diagonal_maior * diagonal_menor)/2;

print(100*"-")
D = float(input("Insira o valor da diagonal maior: "))
d = float(input("Insira o valor da diagonal menor: "))

area = (D * d)/2

print(100*"-")
print(f"A área do losangulo é de: {area}")
